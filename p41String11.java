class p41String11{
	
	public static void main(String args[]){
		
		String s1 = "Hey! I Wanna Change Alphabet Case";
        
       System.out.println("Original String is: "+s1);
	   
	   //now change everything to lower case using toLowerCase()
	   
	   System.out.println("toLowerCase Result: "+s1.toLowerCase());	

       //now change everything to upper case using toUpperCase()	   
		  
       System.out.println("toUpperCase Result is:"+s1.toUpperCase());   		
	}
	
	
}