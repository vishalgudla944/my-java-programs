class p38String9{
	
	public static void main(String args[]){
		
	String	s1 = "We Welcome You to War";
		 
		 //Replacing Substring 'W' with 'e'
		
	String	s2 = s1.replace('W','e');
		
		//NOTE: If we give small w it won't change anything.
		
		System.out.println("Original String is: "+ s1);
		System.out.println("Replaced String is: "+ s2);
		
	}
	
}