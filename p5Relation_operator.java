class p5Relation_operator
{
	public static void main(String args[])
	{
		
		byte x=20;
		byte y=30; 
		
		System.out.println("x>y:"+(x>y));
		System.out.println("x>=y:"+(x>=y));
		System.out.println("x<y:"+(x<y));
		System.out.println("x<y:"+(x<=y));
		System.out.println("x==y:"+(x==y));
		System.out.println("x!=y:"+(x!=y));
		
		
		
		
	}
}